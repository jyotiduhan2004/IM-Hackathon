"""One-shot repair for the 333 people-stubs created 2026-04-24 by the
viewer-fixes sweep — fix their malformed titles and upsert into `wiki_pages`.

Problem (found in the post-sweep audit):
- Titles were generated by title-casing the slug ("Abhinay Kumar1",
  "Aditya Chaudhary01" with the numeric email-prefix bleeding into the
  display name).
- `is_external` was missing, breaking the canonical schema in
  `src/compile/entities.py::_stub_markdown`.
- DB `wiki_pages` row wasn't upserted, so the catalog doesn't know these
  pages exist.

Fix (per user direction 2026-04-24: "the email is converted to kebab case;
that is the way it should be used") — match the canonical `_stub_markdown`
convention: when no display_name is available, `title = email`. That's the
honest identity; a human can enrich it later.

Also moves two mis-categorized system-stubs
(`wiki/systems/alok-kumar2.md`, `wiki/systems/deepak-yadav01.md`) to
`wiki/people/` — they're person-shaped, flagged in the audit.

Lifecycle: one-shot — retire after 2026-05-24 (expiry gate: no stubs
with `updated_by: viewer-fixes-stub-sweep` have malformed titles).
"""

from __future__ import annotations

import sys
from datetime import UTC
from datetime import datetime
from pathlib import Path

import click
import yaml

REPO_ROOT = Path(__file__).resolve().parent.parent
if str(REPO_ROOT) not in sys.path:
    sys.path.insert(0, str(REPO_ROOT))

from src.db import connect  # noqa: E402
from src.db.users import upsert_user  # noqa: E402
from src.db.wiki_pages import upsert_wiki_page  # noqa: E402

# Person-shaped pages that landed in `systems/` but represent individuals.
# Identified by audit 2026-04-24; extend when reviewer catches more.
MISCATEGORIZED_PERSON_SYSTEMS = ("alok-kumar2", "deepak-yadav01")


def slug_to_email(slug: str) -> str:
    """Best-effort inverse of `src/compile/entities.py::email_to_slug`.

    `abhinay-kumar1-indiamart-com` → `abhinay.kumar1@indiamart.com`.
    Plain slugs (no `-indiamart-com` suffix) are assumed internal — we
    append `@indiamart.com` since every such stub we've seen was
    referenced from an internal topic/system page.

    Caveat (flagged by Codex 2026-04-24 review): `email_to_slug` is NOT
    bijective. It collapses every non-alphanumeric run to `-` and
    supports `+` addresses, so multiple emails can map to the same slug
    (`foo.bar@` and `foo-bar@` both become `foo-bar-…`). The inverse we
    reconstruct here swaps every `-` in the local part for `.`, which is
    correct for the IndiaMART-dotted-form majority but would miss
    `foo+tag@` / `foo-bar@` shapes. Mitigation: if a same-slug row with
    a different `canonical_user_email` already lives in `users`, prefer
    that email (catalog-truth beats reconstruction). Non-mitigated
    cases get a best-effort email the next compile can correct on a
    real From/To observation.
    """
    local = slug.removesuffix("-indiamart-com")
    return f"{local.replace('-', '.')}@indiamart.com"


def canonical_stub_markdown(slug: str, *, now_iso: str) -> str:
    """Render the stub content, matching `src/compile/entities.py::_stub_markdown`
    plus the timestamp/updater fields the coordinator stamps post-compile.
    """
    email = slug_to_email(slug)
    fm = {
        "title": email,
        "page_type": "person",
        "status": "active",
        "email": email,
        "is_external": False,
        "sources": [],
        "related": [],
        "last_compiled": now_iso,
        "updated_by": "viewer-fixes-stub-sweep",
        "update_count": 1,
    }
    yaml_block = yaml.safe_dump(
        fm, sort_keys=False, allow_unicode=True, width=120
    ).rstrip()
    body = (
        f"Email: {email}\n"
        "\n"
        "<!-- stub repaired by repair_viewer_fixes_stubs 2026-04-24; "
        "next compile will enrich with backlinks + source threads -->\n"
    )
    return f"---\n{yaml_block}\n---\n\n{body}"


def page_needs_repair(path: Path) -> bool:
    """True if the page was created by the 2026-04-24 stub sweep."""
    try:
        text = path.read_text(encoding="utf-8")
    except OSError:
        return False
    return "updated_by: viewer-fixes-stub-sweep" in text


@click.command()
@click.option("--dry-run", is_flag=True, help="Preview changes without writing.")
def main(dry_run: bool) -> None:
    wiki = REPO_ROOT / "wiki"
    people = wiki / "people"
    systems = wiki / "systems"
    now_iso = datetime.now(UTC).isoformat()

    rewrites = []
    moves = []

    for path in sorted(people.glob("*.md")):
        if not page_needs_repair(path):
            continue
        rewrites.append(path)

    for slug in MISCATEGORIZED_PERSON_SYSTEMS:
        src = systems / f"{slug}.md"
        if src.exists():
            moves.append((src, people / f"{slug}.md"))

    click.echo(f"stubs to repair: {len(rewrites)}")
    click.echo(f"systems→people moves: {len(moves)}")

    if dry_run:
        if rewrites:
            click.echo(f"  sample slug: {rewrites[0].stem}")
            click.echo(f"  → title: {slug_to_email(rewrites[0].stem)}")
        for src, dst in moves:
            click.echo(f"  mv {src.relative_to(REPO_ROOT)} {dst.relative_to(REPO_ROOT)}")
        return

    def _resolve_email(conn: object, slug: str, guessed_email: str) -> str:
        """Return the email to use for this slug's `canonical_user_email`.

        Catalog-truth beats reconstruction: if `wiki_pages` already has
        a row for this slug with a non-null `canonical_user_email`,
        return that (the compiler observed a real From/To header — our
        `slug_to_email` guess might be lossier). Otherwise return the
        guess. Fixes the re-run data-integrity hazard Codex flagged on
        2026-04-24 round-2 review: `upsert_wiki_page`'s ON CONFLICT
        overwrites `canonical_user_email` unconditionally, so a second
        run of this script with a worse guess would clobber real truth.
        """
        row = conn.execute(
            "SELECT canonical_user_email FROM wiki_pages WHERE slug = %s",
            (slug,),
        ).fetchone()
        if row and row.get("canonical_user_email"):
            return str(row["canonical_user_email"])
        return guessed_email

    with connect() as conn:
        for path in rewrites:
            slug = path.stem
            guessed = slug_to_email(slug)
            path.write_text(canonical_stub_markdown(slug, now_iso=now_iso))
            email = _resolve_email(conn, slug, guessed)
            # wiki_pages.canonical_user_email has an FK to users.email —
            # seed the user row only when we're using the guessed email
            # (a catalog-preserved email is already in users by definition).
            if email == guessed:
                upsert_user(conn, email=email)
            upsert_wiki_page(
                conn,
                slug=slug,
                path=f"wiki/people/{slug}.md",
                title=email,
                page_type="person",
                status="active",
                canonical_user_email=email,
            )

        for src, dst in moves:
            slug = src.stem
            guessed = slug_to_email(slug)
            # Overwrite as a canonical person stub — the original system-page
            # body was auto-generated "Stub page auto-created" boilerplate
            # anyway; no signal lost.
            dst.write_text(canonical_stub_markdown(slug, now_iso=now_iso))
            src.unlink()
            email = _resolve_email(conn, slug, guessed)
            if email == guessed:
                upsert_user(conn, email=email)
            upsert_wiki_page(
                conn,
                slug=slug,
                path=f"wiki/people/{slug}.md",
                title=email,
                page_type="person",
                status="active",
                canonical_user_email=email,
            )
        conn.commit()

    click.echo(f"✓ repaired {len(rewrites)} stubs")
    click.echo(f"✓ moved {len(moves)} systems/ → people/")
    click.echo(f"✓ upserted {len(rewrites) + len(moves)} rows into wiki_pages")


if __name__ == "__main__":
    main()
